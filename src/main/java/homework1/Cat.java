package homework1;

public class Cat {
    private String name;
    private int age;
    private String colorFur;

    public Cat(String name, int age, String colorFur) {
        this.name = name;
        this.age = age;
        this.colorFur = colorFur;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getColorFur() {
        return colorFur;
    }
}
